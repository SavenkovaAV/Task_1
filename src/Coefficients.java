public class Coefficients {
    public int a;
    public int quantityOfNumbers;

    public Coefficients(int a, int quantityOfNumbers) {
        this.a=a;
        this.quantityOfNumbers = quantityOfNumbers;
    }

    public int getA() {
        return a;
    }

    public int getQuantityOfNumbers() {
        return quantityOfNumbers;
    }
}
